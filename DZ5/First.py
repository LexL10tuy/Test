day = int(input('Введите день недели '))
if day == 1:
    print('Monday')
elif day == 2:
    print('Thuesday')
elif day == 3:
    print('Wendsday')
elif day == 4:
    print('Thurtuday')
elif day == 5:
    print('Friday')
elif day == 6:
    print('Sunday')
elif day == 7:
    print('Saturday')
else:
    print('Нет такого дня недели')